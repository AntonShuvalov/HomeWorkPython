n = int(input("Введите номер: "))
if n == 1:
    print(n, '->', "Понедельник")
elif n == 2:
    print(n, '->', "Вторник")
elif n == 3:
    print(n, '->', "Среда")
elif n == 4:
    print(n, '->', "Четверг")
elif n == 5:
    print(n, '->', "Пятница")
elif n == 6:
    print(n, '->', "Суббота")
elif n == 7:
    print(n, '->', "Воскресенье")
else:
    print(n, '->', "Нет такого дня")
